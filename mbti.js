function recommendFood() {
  const mbti = document.getElementById("mbtiInput").value.toUpperCase().trim();
  const result = document.getElementById("mbtiResult");

  if (!mbtiFoodMap[mbti]) {
    result.style.display = "block";
    result.innerHTML = "<p style='color: #c0392b;'>올바른 MBTI를 입력해주세요. 예: ENFP, ISTJ</p>";
    return;
  }

  const foodList = mbtiFoodMap[mbti];
  const food = foodList[Math.floor(Math.random() * foodList.length)];
  const calorie = foodCalories[food] || "정보 없음";
  const reason = mbtiReasons[mbti];

  result.style.display = "block";
  result.innerHTML = `
    <div style="animation: fadeInUp 0.5s ease-out;">
        <p style="font-size: 14px; color: #666;">당신의 MBTI는 <strong>${mbti}</strong></p>
        <div style="font-size: 30px; font-weight: bold; color: #e64a19; margin: 15px 0;">🍽️ ${food}</div>
        <p><strong>칼로리:</strong> ${calorie} kcal</p>
        <p style="margin-top: 10px; font-style: italic; color: #555;">"${reason}"</p>
    </div>
  `;
}